    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // Botão pesquisar ** =========== **

        String nome = "%" + txtPesquisaFuncionario.getText() + "%";

        FuncionariosDAO dao = new FuncionariosDAO();
        List<Funcionarios> lista = dao.buscarFuncionarios(nome);

        DefaultTableModel dados = (DefaultTableModel) tabelaFuncionarios.getModel();// cria o objeto que vai armazenar os dados da lista
        dados.setNumRows(0);// limpar a lista

        for (Funcionarios c : lista) {
            dados.addRow(new Object[]{ // vai trasformar cada registro em linhas na tabela, na ordem que ira aparecer
                 c.getId(),
                c.getNome(),
                c.getRg(),
                c.getCpf(),
                c.getEmail(),              
                c.getTelefone(),
                c.getCelular(),
                c.getCep(),
                c.getEndereco(),
                c.getNumero(),
                c.getComplemento(),
                c.getBairro(),
                c.getCidade(),
                c.getUf(), 
                c.getCargo(),
                c.getNivelAcesso(),
                c.getSenha()
            });
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

