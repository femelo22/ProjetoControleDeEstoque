    private void txtCpfClienteKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCpfClienteKeyPressed
        // Busca Cliente por CPF
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {// Pesquisa SE pressionar ENTER
            Clientes obj = new Clientes();
            ClientesDAO dao = new ClientesDAO();

            obj = dao.consultaPorCpf(txtCpfCliente.getText());
            

            txtNomeCliente.setText(obj.getNome());
            txtCodigoCliente.setText(Integer.toString(obj.getId()));
        }
    }//GEN-LAST:event_txtCpfClienteKeyPressed

