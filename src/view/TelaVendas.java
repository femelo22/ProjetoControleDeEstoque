    private void btnAdicionarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarItemActionPerformed
        // BTN ADICIONAR ITEM
        
        // 1° passo - Receber os dados e fazer o calculo
        qtd = Integer.parseInt(txtQtdProduto.getText());// Pega a quantidade
        preco = Double.parseDouble(txtPrecoProduto.getText());// Pega o preço
        // 2° passo - Fazer a conta do subtotal de cada item
        subtotal = qtd * preco;        
        // 3° passo - Somar o subtotal de todos item em uma variavel Total da compra
        total += subtotal;
        // 4° passo - Mandar mostrar o total na caixa do TOTAL
        txtTotalVendas.setText(String.valueOf(total));
        
        // 5° passo - Adicionar o produto no carrinho
        carrinho = (DefaultTableModel) tabelaItens.getModel();
        // add os itens dentro do carrinho
        carrinho.addRow(new Object[]{
            txtCodigoProduto.getText(),
            txtNomeProduto.getText(),
            txtQtdProduto.getText(),
            txtPrecoProduto.getText(),
            subtotal
        });
        
    }//GEN-LAST:event_btnAdicionarItemActionPerformed

