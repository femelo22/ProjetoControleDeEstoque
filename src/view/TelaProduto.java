    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
 
        Produtos obj = new Produtos();
        obj.setDescricao(txtDescricao.getText());
        obj.setPreco(Double.parseDouble(txtPreco.getText()));   
        obj.setQtd_estoque(Integer.parseInt(txtQtdEstoque.getText()));
        
        // Criar um objeto de Fornecedores
        Fornecedores f = new Fornecedores();
        f = (Fornecedores) cbFornecedor.getSelectedItem(); // SelectItem pega o objeto e faz um CAST, depois armazena na nossa variavel F
        obj.setFornecedor(f);// Armazena o objeto fornecedor
        
        ProdutosDAO dao = new ProdutosDAO();

        dao.cadastrarProduto(obj);

        new Utilitarios().LimpaTela(painelDados);
    }//GEN-LAST:event_btnSalvarActionPerformed

